package provider;

public class Provider {
	private String provider_ID;
	private String name;
	private String address;
	private String acc_no;
	private int join_fee;
	private int amount_due;
	private int amount_left;
	private String date_joined;
	private String ID;
	private String PW;
	public String getProvider_ID() {
		return provider_ID;
	}
	public void setProvider_ID(String provider_ID) {
		this.provider_ID = provider_ID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getAcc_no() {
		return acc_no;
	}
	public void setAcc_no(String acc_no) {
		this.acc_no = acc_no;
	}
	public int getJoin_fee() {
		return join_fee;
	}
	public void setJoin_fee(int join_fee) {
		this.join_fee = join_fee;
	}
	public int getAmount_due() {
		return amount_due;
	}
	public void setAmount_due(int amount_due) {
		this.amount_due = amount_due;
	}
	public int getAmount_left() {
		return amount_left;
	}
	public void setAmount_left(int amount_left) {
		this.amount_left = amount_left;
	}
	public String getDate_joined() {
		return date_joined;
	}
	public void setDate_joined(String date_joined) {
		this.date_joined = date_joined;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getPW() {
		return PW;
	}
	public void setPW(String pW) {
		PW = pW;
	}
	
	
}
