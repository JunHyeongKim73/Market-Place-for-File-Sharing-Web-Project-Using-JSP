package item;

public class Item_detail {
	private String item_ID;
	private String item_keyword;
	private String machine_required;
	private String os_required;
	private String viewer_need;
	
	public String getItem_ID() {
		return item_ID;
	}
	public void setItem_ID(String item_ID) {
		this.item_ID = item_ID;
	}
	public String getItem_keyword() {
		return item_keyword;
	}
	public void setItem_keyword(String item_keyword) {
		this.item_keyword = item_keyword;
	}
	public String getMachine_required() {
		return machine_required;
	}
	public void setMachine_required(String machine_required) {
		this.machine_required = machine_required;
	}
	public String getOs_required() {
		return os_required;
	}
	public void setOs_required(String os_required) {
		this.os_required = os_required;
	}
	public String getViewer_need() {
		return viewer_need;
	}
	public void setViewer_need(String viewer_need) {
		this.viewer_need = viewer_need;
	}
	
	
}
