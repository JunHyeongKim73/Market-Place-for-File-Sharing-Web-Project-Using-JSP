package item;

public class DroppedItem {
	private String item_ID;
	private String dropped_date;
	private String dropped_reason;
	public String getItem_ID() {
		return item_ID;
	}
	public void setItem_ID(String item_ID) {
		this.item_ID = item_ID;
	}
	public String getDropped_date() {
		return dropped_date;
	}
	public void setDropped_date(String dropped_date) {
		this.dropped_date = dropped_date;
	}
	public String getDropped_reason() {
		return dropped_reason;
	}
	public void setDropped_reason(String dropped_reason) {
		this.dropped_reason = dropped_reason;
	}
	
	
}
